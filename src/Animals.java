public class Animals
{

    private String Name;
    private int Age;

    public void SetName (String name)
    {
        this.Name = name;
    }

    public String GetName ()
    {
        return Name;
    }

    public void SetAge (int age)
    {
        this.Age = age;
    }

    public int GetAge ()
    {
        return Age;
    }
}
